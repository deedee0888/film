function createCardItem(title, year, imgSrc) {
    var articleContainer = document.createElement('article');
    
    var image = document.createElement('img');
    image.src = imgSrc;
    image.style.width = '100%';

    var footer = document.createElement('footer');
    var spanTitle = document.createElement('span');
    var spanYear = document.createElement('span');

    spanTitle.classList.add('title');
    spanYear.classList.add('year');

    spanTitle.innerHTML = title;
    spanYear.innerHTML = year;

    footer.appendChild(spanTitle);
    footer.appendChild(spanYear);

    articleContainer.appendChild(image);
    articleContainer.appendChild(footer);

    return articleContainer;
}

function createMoviesSection(movies, destination, howMany) {
    for(let i = 0; i < howMany; i++) {
        let article = createCardItem(
            movies[i].title,
            movies[i].release_year,
            movies[i].img
        );

        destination.appendChild(article);
    }
}

var generatedSection = document.querySelector('.generator');

